    /**
     * GET - Search mall by location
     *
     * @author Shelgi Prasetyo <shelgi@dominopos.com>
     *
     * List of API Parameters
     * ----------------------
     * @param string latitude
     * @param string longitude
     * @param string distance
     *
     * @return Illuminate\Support\Facades\Response
     */
    public function getSearchMallNearby()
    {
        $httpCode = 200;
        try {
            $lat = OrbitInput::get('latitude', null);
            $long = OrbitInput::get('longitude', null);
            $distance = OrbitInput::get('distance');

            if (empty($distance)) {
                $distance = Config::get('orbit.geo_location.distance', 10);
            }

            $malls = Mall::excludeDeleted()->select('merchants.*')->includeLatLong()->nearBy($lat, $long, $distance);

            // Filter
            OrbitInput::get('keyword_search', function ($keyword) use ($malls) {
                $mainKeyword = explode(" ", $keyword);

                $malls->where(function($q) use ($mainKeyword) {
                    foreach ($mainKeyword as $key => $value) {
                        $q->orWhere(function($r) use ($value) {
                            $r->where('merchants.name', 'like', "%$value%")
                                ->orWhere('merchants.description', 'like', "%$value%")
                                ->orWhere('merchants.city', 'like', "%$value%");
                        });
                    }
                });

            });

            // Get the maximum record
            $maxRecord = (int) Config::get('orbit.pagination.geo_location.max_record');
            if ($maxRecord <= 0) {
                // Fallback
                $maxRecord = (int) Config::get('orbit.pagination.max_record');
                if ($maxRecord <= 0) {
                    $maxRecord = 20;
                }
            }
            // Get default per page (take)
            $perPage = (int) Config::get('orbit.pagination.geo_location.per_page');
            if ($perPage <= 0) {
                // Fallback
                $perPage = (int) Config::get('orbit.pagination.per_page');
                if ($perPage <= 0) {
                    $perPage = 20;
                }
            }

            // Get the take args
            $take = $perPage;
            OrbitInput::get('take', function ($_take) use (&$take, $maxRecord) {
                if ($_take > $maxRecord) {
                    $_take = $maxRecord;
                }
                $take = $_take;

                if ((int)$take <= 0) {
                    $take = $maxRecord;
                }
            });
            $malls->take($take);

            $skip = 0;
            OrbitInput::get('skip', function($_skip) use (&$skip, $malls)
            {
                if ($_skip < 0) {
                    $_skip = 0;
                }

                $skip = $_skip;
            });
            $malls->skip($skip);

            // Default sort by
            $sortBy = 'distance';
            // Default sort mode
            $sortMode = 'asc';

            OrbitInput::get('sortby', function($_sortBy) use (&$sortBy)
            {
                // Map the sortby request to the real column name
                $sortByMapping = array(
                    'mall_name'         => 'merchants.name',
                    'city'              => 'merchants.city',
                    'created_at'        => 'merchants.created_at',
                    'updated_at'        => 'merchants.updated_at',
                    'distance'          => 'distance'
                );

                $sortBy = $sortByMapping[$_sortBy];
            });

            OrbitInput::get('sortmode', function($_sortMode) use (&$sortMode)
            {
                if (strtolower($_sortMode) !== 'asc') {
                    $sortMode = 'desc';
                }
            });
            $malls->orderBy($sortBy, $sortMode);
            
            $_malls = clone $malls;

            $listmalls = $malls->get();
            $count = RecordCounter::create($_malls)->count();

            $this->response->data = new stdClass();
            $this->response->data->total_records = $count;
            $this->response->data->returned_records = count($listmalls);
            $this->response->data->records = $listmalls;
        } catch (ACLForbiddenException $e) {

            $this->response->code = $e->getCode();
            $this->response->status = 'error';
            $this->response->message = $e->getMessage();
            $this->response->data = null;
            $httpCode = 403;
        } catch (InvalidArgsException $e) {

            $this->response->code = $e->getCode();
            $this->response->status = 'error';
            $this->response->message = $e->getMessage();
            $result['total_records'] = 0;
            $result['returned_records'] = 0;
            $result['records'] = null;

            $this->response->data = $result;
            $httpCode = 403;
        } catch (QueryException $e) {

            $this->response->code = $e->getCode();
            $this->response->status = 'error';

            // Only shows full query error when we are in debug mode
            if (Config::get('app.debug')) {
                $this->response->message = $e->getMessage();
            } else {
                $this->response->message = Lang::get('validation.orbit.queryerror');
            }
            $this->response->data = null;
            $httpCode = 500;
        } catch (Exception $e) {

            $this->response->code = $this->getNonZeroCode($e->getCode());
            $this->response->status = 'error';
            $this->response->message = $e->getMessage();
            $this->response->data = null;
            $httpCode = 500;
        }

        $output = $this->render($httpCode);

        return $output;
    }

